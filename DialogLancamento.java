    public DialogLancamento(Frame parent, boolean modal, ArrayList<Lancamento> lista) {
        super(parent, modal);
        initComponents();
        setTitle("Lan√ßamentos");
        DecimalFormat df = new DecimalFormat("#,##0.00"); // formato com 2 casas decimais

        String texto = "";
        for (int i = 0; i < lista.size(); i++) {
            Lancamento l = lista.get(i);
            String tipo = "Despesa";
            String categoria = "";

            if (l instanceof Receita) {
                tipo = "Receita";
                categoria = ((Receita) l).getCategoriaR().name();
            } else if (l instanceof Despesas) {
                categoria = ((Despesas) l).getCategoriaD().name();
            }

            String valorFormatado = df.format(l.getValor());

            texto += "Data: " + l.getData() + " | Valor: R$ " + valorFormatado
                    + " | Categoria: " + categoria + " | Tipo: " + tipo + "\n";
        }

        jTxtArea.setText(texto);
        jTxtArea.setCaretPosition(0);

    }

